<?php


namespace App\StudentCounter;


class Student
{
    private string $name;

    /**
     * Student constructor.
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}
